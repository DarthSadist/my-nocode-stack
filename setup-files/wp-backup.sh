#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è —Ä–µ–∑–µ—Ä–≤–Ω–æ–≥–æ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è WordPress

echo "================================================================="
echo "üîÑ –°–æ–∑–¥–∞–Ω–∏–µ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏ WordPress"
echo "================================================================="

# –°–æ–∑–¥–∞–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –¥–ª—è —Ä–µ–∑–µ—Ä–≤–Ω—ã—Ö –∫–æ–ø–∏–π
BACKUP_DIR="/opt/backups/wordpress"
sudo mkdir -p "$BACKUP_DIR"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–æ–≤ WordPress
if ! sudo docker ps | grep -q "wordpress"; then
  echo "‚ùå WordPress –Ω–µ –∑–∞–ø—É—â–µ–Ω. –ó–∞–ø—É—Å—Ç–∏—Ç–µ —Å–Ω–∞—á–∞–ª–∞ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä WordPress."
  exit 1
fi

if ! sudo docker ps | grep -q "wordpress_db"; then
  echo "‚ùå –ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö WordPress –Ω–µ –∑–∞–ø—É—â–µ–Ω–∞."
  exit 1
fi

# –¢–µ–∫—É—â–∞—è –¥–∞—Ç–∞ –¥–ª—è –∏–º–µ–Ω–∏ —Ñ–∞–π–ª–∞
CURRENT_DATE=$(date +"%Y-%m-%d_%H-%M-%S")

echo "–°–æ–∑–¥–∞–Ω–∏–µ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö..."
# –ü–æ–ª—É—á–µ–Ω–∏–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è –∏–∑ .env —Ñ–∞–π–ª–∞
source /opt/.env

# –°–æ–∑–¥–∞–Ω–∏–µ –¥–∞–º–ø–∞ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö
sudo docker exec wordpress_db sh -c "mysqldump -u${WP_DB_USER} -p${WP_DB_PASSWORD} ${WP_DB_NAME}" > "$BACKUP_DIR/wp_db_$CURRENT_DATE.sql"
if [ $? -eq 0 ]; then
  echo "‚úÖ –î–∞–º–ø –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö —Å–æ–∑–¥–∞–Ω —É—Å–ø–µ—à–Ω–æ: $BACKUP_DIR/wp_db_$CURRENT_DATE.sql"
else
  echo "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –¥–∞–º–ø–∞ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö"
  exit 1
fi

echo "–°–æ–∑–¥–∞–Ω–∏–µ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏ —Ñ–∞–π–ª–æ–≤ WordPress..."
# –†–µ–∑–µ—Ä–≤–Ω–æ–µ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–æ–≤ WordPress
sudo docker exec wordpress tar -czf - /var/www/html > "$BACKUP_DIR/wp_files_$CURRENT_DATE.tar.gz"
if [ $? -eq 0 ]; then
  echo "‚úÖ –†–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è —Ñ–∞–π–ª–æ–≤ —Å–æ–∑–¥–∞–Ω–∞ —É—Å–ø–µ—à–Ω–æ: $BACKUP_DIR/wp_files_$CURRENT_DATE.tar.gz"
else
  echo "‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ —Ä–µ–∑–µ—Ä–≤–Ω–æ–π –∫–æ–ø–∏–∏ —Ñ–∞–π–ª–æ–≤"
fi

# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–∞–≤ –¥–æ—Å—Ç—É–ø–∞ –Ω–∞ –±—ç–∫–∞–ø—ã
sudo chmod -R 600 "$BACKUP_DIR"
sudo chown -R root:root "$BACKUP_DIR"

# –£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä—ã—Ö —Ä–µ–∑–µ—Ä–≤–Ω—ã—Ö –∫–æ–ø–∏–π (—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Ç–æ–ª—å–∫–æ 5 –ø–æ—Å–ª–µ–¥–Ω–∏—Ö)
echo "–£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä—ã—Ö —Ä–µ–∑–µ—Ä–≤–Ω—ã—Ö –∫–æ–ø–∏–π (—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Ç–æ–ª—å–∫–æ 5 –ø–æ—Å–ª–µ–¥–Ω–∏—Ö)..."

# –£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä—ã—Ö –¥–∞–º–ø–æ–≤ –ë–î
ls -t "$BACKUP_DIR"/wp_db_*.sql 2>/dev/null | tail -n +6 | xargs -r sudo rm

# –£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç–∞—Ä—ã—Ö –∞—Ä—Ö–∏–≤–æ–≤ —Ñ–∞–π–ª–æ–≤
ls -t "$BACKUP_DIR"/wp_files_*.tar.gz 2>/dev/null | tail -n +6 | xargs -r sudo rm

echo "================================================================="
echo "‚úÖ –†–µ–∑–µ—Ä–≤–Ω–æ–µ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏–µ WordPress —É—Å–ø–µ—à–Ω–æ –∑–∞–≤–µ—Ä—à–µ–Ω–æ!"
echo "–ë–∞–∑–∞ –¥–∞–Ω–Ω—ã—Ö: $BACKUP_DIR/wp_db_$CURRENT_DATE.sql"
echo "–§–∞–π–ª—ã: $BACKUP_DIR/wp_files_$CURRENT_DATE.tar.gz"
echo "================================================================="
echo "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ —ç—Ç–∏ —Ñ–∞–π–ª—ã –¥–ª—è –≤–æ—Å—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∏—è WordPress –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏."
echo "–î–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ —Ä–µ–∑–µ—Ä–≤–Ω–æ–≥–æ –∫–æ–ø–∏—Ä–æ–≤–∞–Ω–∏—è, –¥–æ–±–∞–≤—å—Ç–µ —ç—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –≤ cron:"
echo "  0 4 * * * /home/$(whoami)/cloud-local-n8n-flowise/setup-files/wp-backup.sh"
echo "–≠—Ç–æ —Å–æ–∑–¥–∞—Å—Ç –µ–∂–µ–¥–Ω–µ–≤–Ω—ã–µ —Ä–µ–∑–µ—Ä–≤–Ω—ã–µ –∫–æ–ø–∏–∏ –≤ 4:00."
echo "================================================================="

exit 0